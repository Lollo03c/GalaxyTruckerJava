package org.mio.progettoingsoft.model.enums;

/**
 * Enumerates the different types of components that can be found or built on a spaceship.
 * This enum provides a clear and categorized way to identify the function of each ship part.
 */
public enum ComponentType {
    PIPE, DRILL, DOUBLE_DRILL, ENGINE, DOUBLE_ENGINE, HOUSING, DEPOT, ENERGY_DEPOT, SHIELD, ALIEN_HOUSING;
}
